
float additionP(float valueA, float valueB)
{
    return valueA + valueB;
}

float substraccionP(float valueA, float valueB)
{
    return valueA - valueB;
}
float multiplicationp(float valueA, float valueB)
{
    return valueA * valueB;
}

float divisionP(float valueA, float valueB)
{
    return valueA / valueB;
}
